'''
题目描述：
运维工程师采集到某产品线网络运行一天产生的日志n条，现需根据日志时间先后顺序对日志进行排序。
日志时间格式为H:M:S.N
H表示小时(0~23)
M表示分钟(0~59)
S表示秒(0~59)
N表示毫秒(0~999) 时间可能并没有补全，也就是说 01:01:01.001也可能表示为1:1:1.1

输入描述：
第一行输入一个整数n表示日志条数1<=n<=100000
接下来n行输入n个时间

输出描述：
按时间升序排序之后的时间；
如果有两个时间表示的时间相同，则保持输入顺序。

示例1：
输入：
2
01:41:8.9
1:1:09.211
输出
1:1:09.211
01:41:8.9
示例2：
输入
3
23:41:08.023
1:1:09.211
08:01:22.0
输出
1:1:09.211
08:01:22.0
23:41:08.023
示例3：
输入
2
22:41:08.023
22:41:08.23
输出
22:41:08.023
22:41:08.23
注：时间相同保持输入顺序
'''
n = int(input())
res = []
for _ in range(n):
    res.append(input())
res = sorted(res, key = lambda x: (int(x.split(':')[0]), int(x.split(':')[1]), int(x.split(':')[2].split('.')[0]), int(x.split('.')[1])))
print('\n'.join(res))